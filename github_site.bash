чтобы сайт был вида
icivi.github.io
надо создать репозиторий с именем icivi.github.io
всё. возможно немножко подождать.

зайди в settings - Pages репозитория (центр страницы) и увидишь 
 'Your site is published at https://icivi.github.io/'


если создать репозиторий с именем icivi
то сайт будет вида 
icivi.github.io/icivi/
причем в этом случае надо зайти в settings и поставить branches master




чтобы скопировать ПАПКИ в мастер ветку можно их ПЕРЕТАЩИТЬ в окно. 
https://github.com/icivi/icivi.github.io/upload/main

но всё равно всякая хрень - частями. 

Поэтому надо юзать 


Потом в GitHub Desktop
делаешь "клонировать". 
Указываешь имя icivi.github.io
укажи пустую папку 'c:\sites\icivi.github.io' 
Потом в эту папку копируешь (переносишь) сайт. 


Потом редактируешь. 
в GitHub Desktop появится "commit", жми